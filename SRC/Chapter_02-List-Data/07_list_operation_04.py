saying = "Don't panic!"
letters = list(saying)
print(letters)

# Every third letter up to by not including index 10
print(letters[0:10:3])

# Skip first 3 letters and then till end
print(letters[3:])

# All letters up to index 10, but not including
print(letters[:10])

# Every Second Letters
print(letters[::2])

# Reverse a list
print(letters[::-1])
